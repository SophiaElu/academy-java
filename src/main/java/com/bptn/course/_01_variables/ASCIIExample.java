package com.bptn.course._01_variables;

public class ASCIIExample {

	public static void main(String[] args) {
		// create a variable of type char
		char myChar= 'A';
		
		//getting the ASCII code of myChar='A'
		int myASCII = myChar;
		
		System.out.println("The ASCII value of "+ myChar + " is " + myASCII);

	}

}
