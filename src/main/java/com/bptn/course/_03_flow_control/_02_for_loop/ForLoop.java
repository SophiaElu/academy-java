package com.bptn.course._03_flow_control._02_for_loop;

public class ForLoop {

	public static void main(String[] args) {
		
		//print values from 1 to 5
		
		for(int i= 5; i>=0 ; i--) {
			System.out.println(i);
		}

	}
}
